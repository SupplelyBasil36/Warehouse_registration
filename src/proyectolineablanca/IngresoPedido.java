    private void btnEliminarSeleccionadoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEliminarSeleccionadoActionPerformed
        // TODO add your handling code here:
        int seleccionado = JTabla.getSelectedRow();
        String cantidadG = String.valueOf(modelo.getValueAt(JTabla.getSelectedRow(), 4));
        String existenciaG = String.valueOf(modelo.getValueAt(JTabla.getSelectedRow(), 1));
        operacion = Integer.parseInt(existenciaG) + Integer.parseInt(cantidadG);
        if (seleccionado >= 0) {
            actualizarBase(operacion);
            modelo.removeRow(seleccionado);
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione el producto que va a quitar");
        }
    }

