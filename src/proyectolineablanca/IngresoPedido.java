    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if (modeloa == null) {
            JOptionPane.showMessageDialog(null, "Escoge un producto de los que tenemos disponibles");
        } else {
            seleccionado = Integer.parseInt(spnCantidad.getValue().toString());
            operacion = existencia - seleccionado;
            Object datos2[] = { modeloa, operacion, precio, ubicacion, seleccionado };
            modelo.addRow(datos2);
            spnCantidad.setValue(1);
            
            actualizarBase(operacion);
        }
    }

    private void tablaAlmacenMouseClicked(java.awt.event.MouseEvent evt) {
        // TODO add your handling code here:
        Object valor_constante = tablaAlmacen.getValueAt(tablaAlmacen.getSelectedRow(), 0);
        try (Connection conn = DriverManager.getConnection(conexion, user, pass)) {
            Statement sts = conn.createStatement();
            sts.execute("SELECT * FROM almacen WHERE modelo = '" + String.valueOf(valor_constante) + "';");
            ResultSet rs = sts.getResultSet();
            if (rs.next()) {
                modeloa = rs.getString("modelo");
                existencia = rs.getInt("cant_Exist");
                precio = rs.getFloat("precio_uni");
                ubicacion = rs.getString("ubicacion");
            }
            conn.close();
        } catch (SQLException ex) {
            System.out.println(ex.getNextException());
        }
    }

